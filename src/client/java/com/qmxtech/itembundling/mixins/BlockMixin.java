package com.qmxtech.itembundling.mixins;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Imports
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

import net.minecraft.block.*;
import org.spongepowered.asm.mixin.Mixin;
import org.spongepowered.asm.mixin.injection.*;
import org.spongepowered.asm.mixin.injection.callback.CallbackInfoReturnable;
import com.qmxtech.itembundling.block.PalletBlock;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// The 'BlockMixin' Class
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

@Mixin( Block.class ) public class BlockMixin
{
// Private Access Members

	// Methods

		@Inject( method = "cannotConnect", at = @At( "HEAD" ), cancellable = true ) private static void cannotConnect(
			BlockState state, CallbackInfoReturnable< Boolean > info
		)
		{
			// Determine if this block should avoid connections to its neighbors.

				if( state.getBlock() instanceof PalletBlock )
					info.setReturnValue( true );
		}
}

