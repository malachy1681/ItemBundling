package com.qmxtech.itembundling;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Imports
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import net.fabricmc.api.ModInitializer;
import com.qmxtech.itembundling.item.*;
import com.qmxtech.itembundling.block.ModBlocks;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// The 'ItemBundling' Class
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

public class ItemBundling implements ModInitializer
{
// Public Access Members

	// Fields

		public static final String MOD_ID = "item-bundling";
		public static final Logger LOGGER = LoggerFactory.getLogger( MOD_ID );

	// Methods

		@Override public void onInitialize()
		{
			// Perform necessary common mod initialization.

				LOGGER.info( ( "Initializing mod '" + MOD_ID + "' ..." ) );
				ModItemGroups.initialize();
				ModItems.initialize();
				ModBlocks.initialize();
				LOGGER.info( ( "Initialization for mod '" + MOD_ID + "' complete." ) );
		}
}

