////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Imports
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

import com.mojang.serialization.MapCodec;
import net.minecraft.block.*;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// The 'PalletBlock' Class
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

public class PalletBlock extends Block
{
// Public Access Members

	// Fields

		public static final MapCodec< PalletBlock > CODEC = createCodec( PalletBlock::new );

	// Constructors

		public PalletBlock( AbstractBlock.Settings settings )
		{
			// Initialize fields.

				super( settings );
		}

	// Methods

		@Override public MapCodec< PalletBlock > getCodec()
		{
			// Return block codec to calling routine.

				return CODEC;
		}
}

